package com.example.testedittext.entities;

public class GroundingDevice {

    private String destination;
    private String place;
    private String distanceToElectrodes;
    private String dopustR;
    private String izmerR;


    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getDistanceToElectrodes() {
        return distanceToElectrodes;
    }

    public void setDistanceToElectrodes(String distanceToElectrodes) {
        this.distanceToElectrodes = distanceToElectrodes;
    }

    public String getDopustR() {
        return dopustR;
    }

    public void setDopustR(String dopustR) {
        this.dopustR = dopustR;
    }

    public String getIzmerR() {
        return izmerR;
    }

    public void setIzmerR(String izmerR) {
        this.izmerR = izmerR;
    }
}
